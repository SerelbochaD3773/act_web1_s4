//"Temporizador con mensaje - Crea una función que imprima un mensaje después de 3 segundos usando setTimeout. (Tipo de función: Función anónima)",
const temporizadorConMensaje = () => {
    setTimeout(() => {
        console.log("¡Este es un mensaje después de 3 segundos!");
    }, 3000);
}

// Ejemplo de uso
temporizadorConMensaje();
